vector<int> xorQueries(vector<int>& arr, vector<vector<int>>& queries) {
    int *dp = new int[arr.size()+1];
    int n = arr.size();
    dp[0] = 0;
    for(int i = 1 ; i <= n; i++){
        dp[i] = dp[i-1]^arr[i-1];
    }   
    vector<int> ans;
    int m = queries.size();
    for(int i = 0 ; i < m ; i++){
        int l = queries[i][0] + 1;
        int r = queries[i][1] + 1;
        ans.push_back(dp[r]^dp[l-1]);
    }
    return ans;
}

